function createVideoCards() {
  const videoSection = document.getElementById("video-section");

  videos.forEach((video) => {
    const videoCard = document.createElement("div");
    videoCard.classList.add("video-card");

    const thumbnail = document.createElement("img");
    thumbnail.src = video.thumbnail;
    thumbnail.alt = "Video Thumbnail";

    const videoInfo = document.createElement("div");
    videoInfo.classList.add("video-info");

    const videoTitle = document.createElement("h4");
    videoTitle.innerText = video.title;

    const videoChannel = document.createElement("p");
    videoChannel.innerText = video.channel;

    const videoDetails = document.createElement("p");
    videoDetails.innerText = `${video.views} â€¢ ${video.uploaded}`;

    videoInfo.appendChild(videoTitle);
    videoInfo.appendChild(videoChannel);
    videoInfo.appendChild(videoDetails);
    videoCard.appendChild(thumbnail);
    videoCard.appendChild(videoInfo);

    videoSection.appendChild(videoCard);
  });
}

document.addEventListener("DOMContentLoaded", () => {
  createVideoCards();
  const createButton = document.querySelector(".icon-create");
  const dropdownMenu = createButton.querySelector(".dropdown-menu");

  createButton.addEventListener("click", () => {
    dropdownMenu.style.display =
      dropdownMenu.style.display === "block" ? "none" : "block";
  });

  document.addEventListener("click", (event) => {
    if (!createButton.contains(event.target)) {
      dropdownMenu.style.display = "none";
    }
  });
});

document.querySelector(".menu-btn").addEventListener("click", function () {
  document.querySelector("aside").classList.toggle("collapsed");
  document.querySelector("main").classList.toggle("collapsed");
  document.querySelector("footer").classList.toggle("collapsed");
  document.querySelector(".categories-navbar").classList.toggle("collapsed");
});
